public class DateTimeInJava8 {
    public static void main(String[] args) {
        System.out.println("Date in Java 8: " + java.time.LocalDate.now());
        System.out.println("Time in Java 8: " + java.time.LocalTime.now());
        System.out.println("Date & Time in Java 8: " + java.time.LocalDateTime.now());
    }
}
